package Day41;

public class Teacher {

private String name;
private String specialty;

//getters and setters

//getter method is just like any other method
//its specifically for getting the fields value and returning it from the method
//its accepts no parameter

public String getName() {
return name;
}

//setter method is just like any other method
//specifically for setting/updating the field value and return nothing
//it accepts one parameter so we can pass the new value
//and return nothing

public void setName(String newName) {
name = newName;
}

public String getSpecialty() {
return specialty;
}

public void setSpecialty(String newSpecialty) {
specialty = newSpecialty;
}






}
